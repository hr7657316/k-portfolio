// Dynamic fetching of projects from GitHub
export const fetchProjects = async () => {
  try {
    const username = 'tesla-kishan'; // Your GitHub username
    const response = await fetch(`https://api.github.com/users/${username}/repos?sort=updated&per_page=10`);
    
    if (!response.ok) {
      throw new Error(`GitHub API error: ${response.status}`);
    }
    
    const repos = await response.json();
    
    return repos.map((repo, index) => ({
      id: index,
      title: repo.name,
      date: new Date(repo.created_at).toLocaleDateString('en-US', { 
        year: 'numeric', 
        month: 'short' 
      }),
      description: repo.description || "No description available",
      image: `https://opengraph.githubassets.com/1/${username}/${repo.name}`,
      tags: repo.topics || [],
      category: repo.topics && repo.topics.includes("android") ? "android app" : 
               repo.topics && repo.topics.includes("ml") ? "machine learning" : "web app",
      github: repo.html_url,
      webapp: repo.homepage || repo.html_url,
    }));
  } catch (error) {
    console.error("Error fetching GitHub projects:", error);
    // Return empty array or fallback to static projects if fetch fails
    return [];
  }
};

